            <h1>Fonctionnalités de CityScope</h1>
            <p>Afin de résoudre ces différents problèmes, CityScope propose plusieurs rubriques afin de garantir une utilisation simple et efficace pour les utilisateurs :</p>
            <ul>
                <li>Communication directe avec la collectivité/mairie via un tchat</li>
                <li>Carte interactive pour signaler les problèmes et proposer des suggestions</li>
                <li>Rubrique "news" de la mairie/commune</li>
            </ul>
            <p>Avant que les problèmes ne soient visibles pour les autres utilisateurs, ils doivent être validés par des administrateurs, des élus ou la mairie de la commune. Cela garantit que seules les informations pertinentes et utiles sont affichées dans l'application.</p>
            <p>Les maires des villes peuvent utiliser CityScope pour détecter rapidement les problèmes de leur ville et les résoudre en un rien de temps. L'interaction entre les citoyens et les maires se fait directement, sans passer par un intermédiaire.</p>
            <p>Le système de vote et de recommandation permet aux utilisateurs de signaler si l'information ou la suggestion sur la carte est utile ou non. Les utilisateurs peuvent voter pour ou contre les suggestions et signaler si les problèmes sont toujours présents ou résolus.</p>
